key_t english::scan(system::hal::drivers::kb::scancode_t code, array<std::special_t> &combination, bool right)
{
    if (combination.size() == 1)
    {
        if (combination[0] == special_t::SFT_L || combination[0] == special_t::SFT_R)
        {
            key_t r;
            if (!right) r = _layout_sft[code.key];
            else r = _specials_right[code.key];
            r.focus = (focus_t)code.release;
            return r;
        }
        else if (combination[0] == special_t::ALT_R)
        {
            key_t r = _layout_alt_gr[code.key];
            r.focus = (focus_t)code.release;
            return r;
        }
    }
    else if (combination.size() == 2)
    {
        if ((combination[0] == special_t::ALT_R && combination[1] == special_t::CTRL_R) || (combination[0] == special_t::CTRL_R && combination[1] == special_t::ALT_R))
        {
            key_t r;
            if (!right) r = _layout_sft[code.key];
            else r = _specials_right[code.key];
            r.focus = (focus_t)code.release;
            return r;
        }
    }
    
    key_t r = _layout[code.key];
    if (!right) r = _layout[code.key];
    else r = _specials_right[code.key];
    r.focus = (focus_t)code.release;

    return r;
}